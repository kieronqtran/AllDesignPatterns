package practice.memento.basic;

public class EmployeeMemento {
  private String name;
  private int age;

  public EmployeeMemento() {
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }
}
